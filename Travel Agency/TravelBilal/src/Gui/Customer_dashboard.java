    private void search_custActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_custActionPerformed
        // TODO add your handling code here:
        conn = Connect.ConnectDB();
        try{
            String query="select cust_id,city_code,cust_name,cust_phone,cust_cnic,cust_email from customer where cust_id='"+cust_id.getText()+"'";
            PreparedStatement pst=conn.prepareStatement(query);
            rs=(OracleResultSet) pst.executeQuery();
            if(rs.next()){
                cust_id.setText(rs.getString(1));
                city_code.setText(rs.getString(2));
                cust_name.setText(rs.getString(3));
                cust_phone.setText(rs.getString(4));
                cust_cnic.setText(rs.getString(5));
                cust_email.setText(rs.getString(6));
            }
            else
            {
                JOptionPane.showMessageDialog(this, "ID not found!");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_search_custActionPerformed

