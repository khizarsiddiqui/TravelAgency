    private void update_custActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_custActionPerformed
        // TODO add your handling code here:
        conn = Connect.ConnectDB();
        try{
            String query="update Customer set cust_name=?,cust_email=?,cust_phone=?,cust_CNIC=?,city_code=? where cust_id=?";
            PreparedStatement pst=conn.prepareStatement(query);
            pst.setString(1,cust_name.getText());
            pst.setString(2,cust_email.getText());
            pst.setString(3,cust_phone.getText());
            pst.setString(4,cust_CNIC.getText());
            pst.setString(5,city_code.getText());
            pst.setString(6,cust_id.getText());
            rs=(OracleResultSet) pst.executeQuery();
            if(rs.next())
            {
                JOptionPane.showMessageDialog(this, "Updated!");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Not Updated!");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_update_custActionPerformed

