    private void search_custActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_custActionPerformed
        // TODO add your handling code here:
        conn = Connect.ConnectDB();
        try{
            String query="select cust_name,cust_email,cust_phone,cust_CNIC,city_code from Customer where cust_id='"+cust_id.getText()+"'";
            PreparedStatement pst=conn.prepareStatement(query);
            rs=(OracleResultSet) pst.executeQuery();
            if(rs.next()){
                cust_name.setText(rs.getString(1));
                cust_email.setText(rs.getString(2));
                cust_phone.setText(rs.getString(3));
                cust_CNIC.setText(rs.getString(4));
                city_code.setText(rs.getString(5));
            }
            else
            {
                JOptionPane.showMessageDialog(this, "ID not found!");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_search_custActionPerformed

