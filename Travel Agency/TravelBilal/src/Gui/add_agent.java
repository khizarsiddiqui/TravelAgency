    private void insert_agentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insert_agentActionPerformed
        // TODO add your handling code here:
        conn = Connect.ConnectDB();
        try{
            String query="insert into Agent"
                    +"(agent_id,city_code,agent_name,agent_user,agent_pass,agent_phone)"
                    +"values(?,?,?,?,?,?)";
            PreparedStatement pst=conn.prepareStatement(query);
            pst.setString(1,Agent_id.getText());
            pst.setString(2,city_code.getText());
            pst.setString(3,Agent_name.getText());
            pst.setString(4,Agent_user.getText());
            pst.setString(5,Agent_pass.getText());
            pst.setString(6,Agent_phone.getText());
            rs=(OracleResultSet) pst.executeQuery();
            if(rs.next())
            {
                JOptionPane.showMessageDialog(this, "Inserted!");
                String qu="select max(agent_id) from agent";
                PreparedStatement pst1=conn.prepareStatement(qu);
                rs=(OracleResultSet) pst1.executeQuery();
                if(rs.next())
                {
                    JOptionPane.showMessageDialog(this, "Your Agent ID is: "+rs.getString(1));
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Error showing in ID");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Not Inserted!");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_insert_agentActionPerformed

